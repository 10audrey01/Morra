package nz.ac.auckland.se281;

public class FingersAndSum {
  private int fingers;
  private int sum;

  public FingersAndSum(int fingers, int sum) {
    this.fingers = fingers;
    this.sum = sum;
  }

  public int getFingers() {
    return fingers;
  }

  public int getSum() {
    return sum;
  }
}
